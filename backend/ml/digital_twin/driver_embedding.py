"""DriverEmbeddingManager — Digital Twin Vector Engine.

Manages the full lifecycle of driver behavioral embeddings generated by the
21-D EmbeddingModel encoder. These embeddings are distinct from the MobileFaceNet
facial embeddings — they represent a driver's *behavioral* signature (driving
patterns, stress response, attention tendencies) rather than their face.

Responsibilities:
    - Generate 128-D session embeddings from feature matrix sequences.
    - Incrementally update embeddings as new sessions accumulate.
    - Perform cosine similarity and Euclidean distance comparisons.
    - Support KNN search across the driver corpus for cluster assignment.
    - Persist embeddings to/from the DriverEmbedding database table.
    - Compute embedding quality scores from calibration completeness.
    - Export embeddings to JSON and NumPy for analytics pipelines.

Embedding types in CogniDrive:
    - **Face embedding** (MobileFaceNet, 128-D/512-D) — who the driver is.
    - **Behavioral embedding** (EmbeddingModel, 128-D) — how the driver behaves.

Both are stored in the same ``DriverEmbedding`` table but serve different purposes.
This manager specifically handles behavioral embeddings.
"""

