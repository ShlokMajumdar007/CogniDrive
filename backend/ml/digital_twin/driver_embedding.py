# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

EMBEDDING_DIM: int = 128
MIN_QUALITY_SAMPLES: int = 5     # Sessions needed for high-quality embedding
MAX_QUALITY_SAMPLES: int = 50    # Samples above this give diminishing returns
_COLD_START_WEIGHT: float = 0.3  # Weight for cold-start random-projection initialization


# ---------------------------------------------------------------------------
# Welford Online Statistics State
# ---------------------------------------------------------------------------


class WelfordState:
    """Numerically stable online mean and variance via Welford's algorithm.

    Supports vector/scalar states represented as NumPy arrays.
    """

    def __init__(self, mean: np.ndarray, m2: np.ndarray, count: int = 0) -> None:
        self.mean = np.array(mean, dtype=np.float64)
        self.m2 = np.array(m2, dtype=np.float64)
        self.count = count

    def update(self, val: np.ndarray) -> None:
        """Updates the running mean and sum of square deviations with a new sample."""
        val = np.array(val, dtype=np.float64)
        self.count += 1
        delta = val - self.mean
        self.mean += delta / self.count
        delta2 = val - self.mean
        self.m2 += delta * delta2

    @property
    def variance(self) -> np.ndarray:
        """Computes the sample variance."""
        if self.count < 2:
            return np.zeros_like(self.mean)
        return self.m2 / (self.count - 1)

    @property
    def std(self) -> np.ndarray:
        """Computes the sample standard deviation."""
        return np.sqrt(np.maximum(self.variance, 0.0))


